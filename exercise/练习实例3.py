'''
一个整数，它加上100后是一个完全平方数，再加上168又是一个完全平方数，
请问该数是多少？
'''

'''
(x+100)^2 = n^2               # x+100 = n^2
(x+168)^2 = m^2               # x+168 + m^2

x^2+200x+10000=n^2
x^2+336x+28224=m^2
...

数学太差，不会做
'''
#也是小白,代码也只能写成这样，不知道会不会有更好的
import math
x = 0
while 1:
	if math.sqrt(x+100)%1 == 0 and math.sqrt(x+168)%1 == 0:
		print(x)
		break
	else:
		x += 1
